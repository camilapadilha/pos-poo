    ArrayList<Integer> idCidades = new ArrayList<>();

    private void preencherComboCidades() {
        try {
            Connection conn = Banco.abrirConexao();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM cidade");
            ResultSet rs = ps.executeQuery();
            idCidades.add(-1);
            while (rs.next()) {
                idCidades.add(rs.getInt("id"));
                comboCidades.addItem(rs.getString("nome"));
            }

            ps.close();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void limpaCampos() {
        campoBairro.setText("");
        campoCpf.setText("");
        campoEndereco.setText("");
        campoFone.setText("");
        campoNome.setText("");
        campoNumero.setText("");
        campoRg.setText("");
    }

    private Boolean validaCampos() {
        String mensagem = "";
        Boolean retorno = true;
        if (campoNome.getText().equals("")) {
            mensagem = mensagem + "O campo nome √© obrigat√≥rio!\n";
            retorno = false;
        }
        if (campoCpf.getText().equals("")) {
            mensagem = mensagem + "O campo Cpf √© obrigat√≥rio!\n";
            retorno = false;
        }
        if (campoFone.getText().equals("")) {
            mensagem = mensagem + "O campo Telefone √© obrigat√≥rio!\n";
            retorno = false;
        }
        if (retorno == false) {
            JOptionPane.showMessageDialog(null, mensagem);
        }
        return retorno;
    }
